export function getUsers() {
    return new Promise((res, rej) => {
        setTimeout(() => {
            let userArr = []
            users.forEach(e => {
                let obj = {}
                obj.name = e.name
                obj.id = e.id
                userArr.push(obj)
                obj = {}
            })
            res([...userArr])
        }, 1000)
    })
}

export function userDetails(id) {
    return new Promise((res, rej) => {
        setTimeout(() => {
            users.forEach(d => {
                if (id === d.id) {
                    res(d)
                }
            })
        }, 300)
    })
}

